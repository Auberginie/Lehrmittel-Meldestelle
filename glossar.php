								    <h3 class="accordion-button">Sexismus<span class="toggle"></span></h3>
								    <div class="accordion-content" style="max-height: 0px;">
								      <p>Die Brava-Organisation erklärt den Begriff Sexismus mit folgenden Worten:  Sexismus umfasst die Zuschreibung von Eigenschaften basierend auf Geschlechtervorstellungen sowie die Diskriminierung von Menschen aufgrund von Geschlecht. Dadurch werden Menschen in ihrer freien Entfaltung gehindert. Sexismus kann alle Menschen betreffen, aber auch heute noch sind Frauen weltweit – und auch in der Schweiz – stärker betroffen.</p>
									  <p>Sexismus dient als Grundlage eines patriarchalen Systems, welches Menschen in zwei Geschlechter einteilt und hierarchisch ordnet. In diesem System wird Frauen eine untergeordnete Position zugeschrieben und Männern eine privilegierte, machtvolle Position. Sexismus durchdringt jeden Bereich der Gesellschaft und zeigt sich sowohl in alltäglichen Handlungen in Form sexistischer Witze oder Herabwürdigungen wie auch strukturell. Er ist tief in unserer Gesellschaft verankert, etwa in Arbeitswelt, Politik und Medien (sexistische Stereotype in der Werbung, geringe Chancen auf dem Arbeitsmarkt, Lohnungleichheit, ungleiche Verteilung der unbezahlten Arbeit, Untervertretung von Frauen in der Politik und in Entscheidungspositionen).</p>
									  <p>In weiten Teilen der Gesellschaft hält sich hartnäckig die Vorstellung, es gebe nur zwei Geschlechter und diese seien von Natur aus unterschiedlich. Dabei wird Männlichkeit auf- und Weiblichkeit abgewertet, was die Diskriminierung von Frauen legitimiert.</p>
									  <p>Wir kritisieren Vorstellungen und Bilder davon, wie sich ein Mensch oder eine Gruppe von Menschen aufgrund von Geschlecht, Geschlechtsidentität oder sexueller Orientierung zu verhalten hat. Erlebte Diskriminierung ist keine Erfahrung von Einzelpersonen, sondern hat System und trifft alle, die die gesellschaftlichen Vorstellungen nicht erfüllen.</p>
									  <p>Sexismus steht mit anderen Formen von Diskriminierung in Wechselwirkung. So machen Women of Color andere Sexismuserfahrungen als weisse Frauen, Frauen mit Behinderung andere als Frauen ohne Behinderung. </p>

