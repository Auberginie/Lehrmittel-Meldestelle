								    <div class="accordion-content" style="max-height: 0px;">
								      <h5>Definition:</h5> 
								      <p>Stereotypisierung ist ein kognitiver Prozess, bei dem Einzelpersonen aufgrund von Merkmalen wie Geschlecht, Rasse, Religion oder körperlichen Merkmalen Annahmen über eine Person oder Gruppe treffen. Dabei geht es darum, bestimmte Merkmale auf eine ganze Gruppe zu verallgemeinern, was zu festen und übermässig verallgemeinerten Überzeugungen führt.</p>
									  <h5>Positive Stereotypisierung:</h5>
									  <p>Positive Stereotype sind weniger offensichtlich und zielen möglicherweise nicht darauf ab, der betroffenen Gruppe zu schaden. Sie können jedoch dennoch negative Folgen haben, indem sie Druck erzeugen, sich dem Stereotyp anzupassen.</p>
									  <h5>Beispiele für positive Stereotypisierung:</h5>
									  <p>Beispiele hierfür sind die Überzeugung, dass bestimmte ethnische Gruppen in bestimmten Disziplinen hervorragende Leistungen erbringen oder dass Personen mit einem bestimmten Hintergrund in bestimmten Bereichen über überlegene Fähigkeiten verfügen.</p>
