                    <p>Konsequenzen für die Forschungsethik</p>
                    <p>«Geschlechtstypisches» Verhalten und ein statisches Verständnis von Zweigeschlechtlichkeit sind nicht naturgegeben, sondern werden permanent reproduziert («doing gender») – unter anderem dadurch, dass Erziehungsberechtigte, Schüler_innen, Lehrpersonen oder Forscher_innen heteronormative Zuordnungen vornehmen. Daher: Forschung am IAE verzichtet auf die reflexhafte Einteilung in «Mädchen» und «Jungen». Geschlecht wird als Analysekategorie verwendet, nicht als naturgegebene Eigenschaft. Das heisst, es wird gefragt, auf welche Weise sich geschlechtliche Zuschreibungen (nicht: «das Geschlecht») jeweils auf Lehren und Lernen auswirken. Geschlecht wird ausserdem intersektionell betrachtet, das heisst im Zusammenwirken mit anderen Kategorien wie soziale Herkunft oder Bildungshintergrund.</p>
                
