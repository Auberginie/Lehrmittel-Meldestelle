								    <div class="accordion-content" style="max-height: 0px;">
								      <p> Menschen mit Behinderung treffen im Alltag häufig auf Ableismus. Andere Leute begegnen ihnen dann mit bestimmten Vorannahmen, ohne die Personen direkt zu fragen. Zum Beispiel danach, was sie können oder nicht können. Wir erklären euch die Definition von Ableismus und wie du damit umgehen kannst.</p>
									  <p> Werden Menschen im Alltag auf ihre körperliche oder psychische Behinderung reduziert, spricht man in der Fachsprache von Ableismus. Genauer bedeutet Ableismus also, dass Menschen mit Behinderung von anderen Menschen ohne Behinderung auf die Merkmale reduziert werden, in denen sie sich vom vermeintlichen Normalzustand unterscheiden. Dies können zum Beispiel sichtbare oder unsichtbare Merkmale sein, also ein Rollstuhl oder eine geistige Behinderung. Von diesen Merkmalen wird anschließend, ohne vorher nachzufragen, beispielsweise darauf geschlossen, was die Person mit der Behinderung kann oder nicht kann oder wie sich die Person fühlt. Das ist eine Art von Diskriminierung. Diese Definition wurde von Aktion Mensch verfasst.</p>
