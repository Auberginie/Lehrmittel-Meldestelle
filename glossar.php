                <h3>Rassismus</h3>
                <p>Die Eidgenössische Kommission gegen Rassismus EKR definiert Rassismus wie folgt: 
                    Vier Elemente sind für rassistische Ideologien charakteristisch:
                    <ul>
                        <li>Konstruktion und Betonung tatsächlicher oder fiktiver Unterschiede zwischen der Person, die Rassismus ausübt und des Opfers</li>
                        <li>Wertung dieser Unterschiede zum Nutzen der Person, die Rassismus ausübt und zum Schaden des Opfers</li>
                        <li>Verallgemeinerung und Verabsolutierung dieser Unterschiede</li>
                        <li>Legitimierung einer Aggression oder eines Privilegs</li>
                    </ul>

                    Nebst dieser engen Definition, die insbesondere den klassischen Rassismus in seiner biologistischen Prägung beschreibt, gibt es den Rassismusbegriff «im weiteren Sinne», der sich auf kulturelle, psychologische, soziale oder metaphysische Argumente abstützt. Vom Rassismus ist ferner die Rassendiskriminierung zu unterscheiden. Im Gegensatz zu Rassismus, der eine Ideologie oder geistige Haltung bezeichnet, versteht man unter Rassendiskriminierung eine konkrete Handlung, die eine als minderwertig bezeichnete Gruppe oder Einzelperson im realen Leben benachteiligt. Ersteres hat meistens verbale Diffamierung zur Folge, und diese dann konkrete Diskriminierung.</p>
                    Grundsätzlich ist zu beobachten, dass die meisten Fälle rassistischer Diskriminierung in der Schweiz nicht ideologisch begründet sind. Sie basieren vielmehr auf Unwissen, diffusen Ängsten, Aggressionen, Vorurteilen und mangelndem Einfühlungsvermögen. Aber unabhängig davon, ob eine ideologische Bedeutung vorhanden ist oder nicht, geht es beim Einsatz gegen Rassismus zuallererst darum, anzuerkennen, dass es rassistische Diskriminierung gibt, und zwar auf struktureller, institutioneller wie individueller Ebene. Es gilt, die Verletzung, die Opfer derartiger Diskriminierungen erleiden, anzuerkennen.
                
