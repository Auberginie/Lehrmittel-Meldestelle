									  <p>Heteronormativität ist eine Form von und eine Basis für Queerfeindlichkeit, Homophobie und andere Formen der gruppenbezogenen Menschenfeindlichkeit. Der Begriff ist zentral in der Queer-Theorie, die die Naturalisierung und Privilegierung von Heterosexualität und Zweigeschlechtlichkeit in Frage stellt. Das Pendant zur Heteronormativität bezüglich der Geschlechter ist der Androzentrismus.</p>

